//! 打鍵コスト表・運指表(§6、確認事項 #1〜#3 反映)
//!
//! 基準配列は **US(ANSI)固定**(確認事項 #1)。ユーザーの実際の物理配列には
//! 依存させない(ランキングの決定性のため)。§6 の「遠い記号 `\ | ~ \``」の
//! 組分けは US 配列(\ と |、~ と ` が同キー)と整合する。
//!
//! この表・係数を変えたら lib.rs の SCORE_VERSION を必ず上げること。

/// Shift 記号・大文字 → 無シフトの物理キーへ写像(US ANSI)。
/// 運指判定(同キー = 同指)と Shift 要否判定の両方に使う
pub fn unshift(ch: char) -> char {
    match ch {
        'A'..='Z' => ch.to_ascii_lowercase(),
        '!' => '1',
        '@' => '2',
        '#' => '3',
        '$' => '4',
        '%' => '5',
        '^' => '6',
        '&' => '7',
        '*' => '8',
        '(' => '9',
        ')' => '0',
        '_' => '-',
        '+' => '=',
        '{' => '[',
        '}' => ']',
        '|' => '\\',
        ':' => ';',
        '"' => '\'',
        '<' => ',',
        '>' => '.',
        '?' => '/',
        '~' => '`',
        _ => ch,
    }
}

/// US 配列で Shift が必要な文字か
fn needs_shift(ch: char) -> bool {
    unshift(ch) != ch
}

/// 1 キーの基本コスト(§6)。同指連続 ×1.2 は呼び出し側(lib.rs)で掛ける。
///
/// 優先順位: Enter/スペース → 遠い記号 → Shift → 数字段/右端リーチ → 基本。
/// 遠い記号(§6 例示の ` \ | ~)は Shift 記号(~ |)でも 1.8 を優先する。
pub fn key_cost(ch: char) -> f64 {
    match ch {
        // Enter・文中スペースは基本コスト扱い(確認事項 #2)
        '\n' | ' ' => 1.0,
        // 遠い記号(§6 例示: \ | ~ `)。キーボード両端の最遠キー
        '`' | '~' | '\\' | '|' => 1.8,
        // Shift 必要文字(大文字・シフト記号)
        _ if needs_shift(ch) => 1.6,
        // 数字段
        '0'..='9' => 1.3,
        // 数字段の無シフト記号(- =)と右端リーチ([ ])は数字段相当に分類
        // ※ §13 チューニング候補([ ] を 1.0 に下げる等)
        '-' | '=' | '[' | ']' => 1.3,
        // 基本文字: 英小文字
        'a'..='z' => 1.0,
        // ホームポジション周辺の無シフト記号
        ';' | '\'' | ',' | '.' | '/' => 1.0,
        // 表外(非 ASCII 等。文字列内の日本語など)= 遠い記号相当の 1.8
        // ※ 実装時判断【要確認】: IME 入力は実打鍵数が多いが、決定性のため定数に固定
        _ => 1.8,
    }
}

/// 同指連続の連鎖をリセットする文字(確認事項 #3)。
/// Enter を右小指扱いにすると p ; などとの擬似同指が多発するため対象外にする
pub fn resets_chain(ch: char) -> bool {
    ch == '\n' || ch == ' '
}

/// 標準運指(US ANSI・タッチタイピング)での指割り当て。
/// 引数は unshift() 済みの物理キーであること。
/// 0=左小指 1=左薬指 2=左中指 3=左人差指 4=右人差指 5=右中指 6=右薬指 7=右小指。
/// None = 運指が定義できない(スペース・Enter・非 ASCII 等)→ 同指判定の対象外
pub fn finger(key: char) -> Option<u8> {
    match key {
        '`' | '1' | 'q' | 'a' | 'z' => Some(0),
        '2' | 'w' | 's' | 'x' => Some(1),
        '3' | 'e' | 'd' | 'c' => Some(2),
        '4' | '5' | 'r' | 't' | 'f' | 'g' | 'v' | 'b' => Some(3),
        '6' | '7' | 'y' | 'u' | 'h' | 'j' | 'n' | 'm' => Some(4),
        '8' | 'i' | 'k' | ',' => Some(5),
        '9' | 'o' | 'l' | '.' => Some(6),
        '0' | '-' | '=' | 'p' | ';' | '\'' | '/' | '[' | ']' | '\\' => Some(7),
        _ => None,
    }
}